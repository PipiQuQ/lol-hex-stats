// 获取所有英雄名称列表（用于下拉选择）
export function getHeroNames(): string[] {
  return HEROES.map(h => h.name + ' ' + h.title)
}

// 搜索英雄
export function searchHeroes(keyword: string): string[] {
  if (!keyword) return getHeroNames()
  const lower = keyword.toLowerCase()
  return HEROES
    .filter(h => h.name.toLowerCase().includes(lower))
    .map(h => h.name + ' ' + h.title)
}